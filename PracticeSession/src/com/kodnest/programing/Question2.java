package com.kodnest.programing;

public class Question2 {
	public int substractNumber(int a, int b)
	{
		int substract = a - b;
		return substract;
	}
	public int multiplicationNumber(int a, int b)
	{
		int multiplication = a * b;
		return multiplication;
	}
	public int divisionNumber(int a, int b)
	{
		int division = a / b;
		return division;
	}
	public int reminderNumber(int a, int b)
	{
		int reminder = a % b;
		return reminder;
	}
}
